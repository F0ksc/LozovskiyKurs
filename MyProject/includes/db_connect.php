// Параметри підключення до бази даних
$db_host = "localhost";    // Зазвичай "localhost" для XAMPP
$db_user = "root";         // Стандартний користувач MySQL в XAMPP
$db_pass = "";             // Стандартний пароль для користувача "root" в XAMPP - порожній
$db_name = "buzz_db";      // Назва вашої бази даних

// Встановлення з'єднання
$conn = new mysqli($db_host, $db_user, $db_pass, $db_name);

// Перевірка з'єднання
if ($conn->connect_error) {
    // Якщо є помилка, виводимо повідомлення і припиняємо виконання скрипту
    die("Помилка підключення до бази даних: " . $conn->connect_error);
}

// Встановлення кодування з'єднання (дуже важливо для коректного відображення кирилиці)
if (!$conn->set_charset("utf8mb4")) {
    printf("Помилка при встановленні кодування utf8mb4: %s\n", $conn->error);
    // Можна також завершити виконання, якщо кодування критично важливе
    // die("Не вдалося встановити кодування з'єднання.");
}

